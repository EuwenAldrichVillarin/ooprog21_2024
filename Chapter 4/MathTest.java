   class MathTest {
      public static void main(String[] args) {
         System.out.println("SQRT: "+ Math.sqrt(100));
         System.out.println("SIN: "+ Math.sin(45));
         System.out.println("COS: "+ Math.cos(45));
         System.out.println("FLOOR: "+ Math.floor(90));
         System.out.println("CEIL: "+ Math.ceil(90));
         System.out.println("ROUND: "+ Math.round(36.2));
         System.out.println("MAX: "+ Math.max(2,34));
         System.out.println("MIN: "+ Math.min(35,102));
         System.out.println("RANDOM: "+ Math.random());
    }
}